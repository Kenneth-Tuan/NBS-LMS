// 格式化課程時間
export const formatCourseTime = (course) => {
  const days = {
    monday: "週一",
    tuesday: "週二",
    wednesday: "週三",
    thursday: "週四",
    friday: "週五",
  };

  const timeLabels = {
    1: "08:00-08:50",
    2: "09:00-09:50",
    3: "10:10-11:00",
    4: "11:10-12:00",
    5: "13:10-14:00",
    6: "14:10-15:00",
    7: "15:10-16:00",
    8: "16:10-17:00",
    9: "18:00-18:50",
    10: "19:00-19:50",
    11: "20:00-20:50",
    12: "21:00-21:50",
  };

  if (!course.timeSlots || course.timeSlots.length === 0) return "時間未定";

  const times = course.timeSlots.map(
    (ts) => `${days[ts.day]} ${timeLabels[ts.slot]}`
  );

  return times.join("、");
};
